class Apartment {
  final String imageUrl;
  final String owner;
  final String location;
  final String type;
  final String size;
  final double price;
  final double rating;

  const Apartment({
    required this.imageUrl,
    required this.owner,
    required this.location,
    required this.type,
    required this.size,
    required this.price, required this.rating,
  });
}
